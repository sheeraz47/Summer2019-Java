package day03_variables;

public class EscapeSequences {
	/*
	 \-backslah, /-frontslash
	 \n: starts from a new line
	 \t: horizontal tab
	 \\: prints \ backslash
	 \': prints single quote
	 \": prints double quote
	 
	 
	 */
	public static void main(String[] args) {
		
		System.out.println("Hello Cybertek\n Batch12 students");
		System.out.print("Tomorrow is off \n");
		System.out.print("True \n");
		System.out.println("\t Class starts tomorrow");
		System.out.println("\\Aibek");
		System.out.println("\'Java");
		System.out.println("Book is called \"Game of Throne\"");
		System.out.println("Call me \"Aibek\"");
	}
}
